#include "libft.h"

int	ft_strlcpy(char *dst, const char *src, int size)
{
	int	i;
	int	cp_len;

	i = 0;
	cp_len = ft_strlen(src);
	if (size == 0)
		return (cp_len);
	while (src[i] && i < size - 1)
	{
		dst[i] = src[i];
		i++;
	}
	dst[i] = 0;
	return (cp_len);
}
