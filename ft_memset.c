#include "libft.h"

void	*ft_memset(void *s, int c, int n)
{
	const void	*ptr;

	ptr = s;
	while (n--)
	{
		*(unsigned char *)s = c;
		(unsigned char *)s++;
	}
	return ((void *) ptr);
}
